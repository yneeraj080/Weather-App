import React, { use, useEffect, useRef, useState } from 'react'
import './Weather.css'
import search_icon from '../assets/search.png'
import cloud_icon from '../assets/cloud.png'
import rain_icon from '../assets/rain.png'
import snow_icon from '../assets/snow.png'
import clear_icon from '../assets/clear.png'
import drizzle_icon from '../assets/drizzle.png'
import humidity_icon from '../assets/humidity.png'
import wind_icon from '../assets/wind.png'

const Weather = () => {
    const inputRef = useRef();
    const [weatherData, setWeatherData] = useState(null);
    const icons = {
        "01d": clear_icon,
        "01n": clear_icon,
        "02d": cloud_icon,
        "02n": cloud_icon,
        "03n": cloud_icon,
        "03d": cloud_icon,
        "04d": drizzle_icon,
        "04n": drizzle_icon,
        "09d": rain_icon,
        "09n": rain_icon,
        "10d": rain_icon,
        "10n": rain_icon,
        "13d": snow_icon,
        "13n": snow_icon
    }
    const search = async (city) => {
        if(city=="") {
            alert("Please enter a city name");
            return;
        }
        try {
            console.log('API Key:', import.meta.env.VITE_APP_ID); // Debug log
            const url = `https://api.openweathermap.org/data/2.5/weather?q=${city}&units=metric&appid=${import.meta.env.VITE_APP_ID}`;
            const response = await fetch(url);
            const data = await response.json();
            if(!response.ok) {
                alert(data.message || 'Failed to fetch weather data');
            }
            console.log(data);
            const icon = icons[data.weather[0].icon] || clear_icon;
            setWeatherData({
                humidity: data.main.humidity,
                wind: data.wind.speed,
                temperature: Math.floor(data.main.temp),
                location: data.name,
                icon: icon
            });
        } catch (error) {
            setWeatherData(false) ;
            console.error("Error fetching weather data:", error);
        }
    };

    return (
        <div className='weather'>
            <div className='search-bar'>
                <input ref={inputRef} type="text" placeholder='Search' />
                <img src={search_icon} alt="Search" onClick={() => search(inputRef.current.value)} />
            </div>
            {weatherData? <>
            <img src={weatherData?.icon || clear_icon} alt="Weather Icon" className='weather-icon'/>
            <p className='temperature'>{weatherData ? `${weatherData.temperature}Â°C` : '--'}</p>
            <p className='location'>{weatherData ? weatherData.location : 'Loading...'}</p>
            <div className='weather-data'>
                <div className='col'>
                    <img src={humidity_icon} alt="Humidity" />
                    <div>
                        <p>{weatherData ? `${weatherData.humidity}%` : '--'}</p>
                        <span>Humidity</span>
                    </div>
                </div>
                <div className='col'>
                    <img src={wind_icon} alt="Wind" />
                    <div>
                        <p>{weatherData ? `${weatherData.wind} km/hr` : '--'}</p>
                        <span>Wind Speed</span>
                    </div>
                </div>
            </div>  
            </>:<></>}
            
        </div>
    )
}

export default Weather
